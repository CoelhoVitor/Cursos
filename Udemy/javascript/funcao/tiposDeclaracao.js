// Function Declaration
console.log(soma(2, 3))
function soma (x, y){
    return x + y
}

// Function Expression
const sub = function (x, y) {
    return x - y
}
console.log(sub(2, 3))

// Named Function Expression
const mul = function mul (x, y){
    return x * y
}
console.log(mul(2, 3))